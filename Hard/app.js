//Addition
function add (num1, num2)
var sum = num1 + num2;
return sum;
//Subtraction
function subtract (num1, num2)
var difference = num1 - num2;
return difference;
// Multiply
function multiply (num1, num2)
var product = num1 * num2;
return product;
//Division
function division (num1, num2)
var quotient = num1 / num2;
return quotient;